public class Multiplication {

    void print(){
        for (int i=0;i<=10;++i){
            System.out.printf("%d x %d = %d",5,i,5*i).println();

        }
    }
    void print(int multiplier){
        for (int i=0;i<=10;++i){
            System.out.printf("%d x %d = %d",multiplier,i,i*multiplier).println();

        }
    }
}
